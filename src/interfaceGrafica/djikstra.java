    private void jBtnVaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnVaiActionPerformed
        // TODO add your handling code here:
        boolean pronto = true;
        if (!carregado) {
            JOptionPane.showMessageDialog(null, "Grafo em carregamento...");
            pronto = false;
        }
        if ("".equals(jTextVerticeInicial.getText())) {
            JOptionPane.showMessageDialog(null, "Selecione um vértice inicial");
            pronto = false;

        }
        int v = Integer.parseInt(jTextVerticeInicial.getText());
        if (v < 0 || v > g.numeroDeVertices()) {
            JOptionPane.showMessageDialog(null, "Selecione um vértice válido");
            pronto = false;
        }

        if (pronto) {
            try {
                b = new grafos.Djikstra(g, v);
                b.runDjikstra();
                limpaTabela();
                populaBFS();
                jLabelVerticeInicial.setText(jTextVerticeInicial.getText());
            } catch (Exception ex) {
                Logger.getLogger(djikstra.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jBtnVaiActionPerformed

