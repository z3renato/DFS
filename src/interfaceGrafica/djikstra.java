    public void populaBFS() {
        DefaultTableModel table = (DefaultTableModel) jTableBFS.getModel();
        int pai, distancia;
        boolean acessivel;
        for (int i = 0; i < g.numeroDeVertices(); i++) {
            pai = b.pai[i];
            distancia = (int) b.d[i];
            //acessivel = b.acessivel(i);
            table.addRow(new Integer[]{i, pai, distancia});

        }
    }
